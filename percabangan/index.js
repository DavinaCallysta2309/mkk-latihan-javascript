let number = prompt('masukan angka pertama');
 let number1 = prompt('masukan angka kedua')

 if (number > number1){
    alert(`${number} lebih besar dari ${number1}`);
 }
 else if (number < number1){
    alert(`${number} lebih kecil dari ${number1}`);
 }
 else{
    alert(`${number} sama dengan ${number1}`);
 }
